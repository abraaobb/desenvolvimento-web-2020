// + - adição
// - - subtração
// * - multiplicação
// / - divisão
// % - modulo
// ++ - incremeto
// -- - decremento

let a = 4;
let b = 2;

alert(a + b);
alert(a - b);
alert(a * b);
alert(a / b);
alert(a % b);

alert(++a);
alert(a++);
alert(a);

// += = atribui mais um valor
// -= = atribui mais um valor
// *= = atribui mais um valor
// /= = atribui mais um valor
// %= = atribui mais um valor

